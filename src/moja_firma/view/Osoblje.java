    private void akcijaObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_akcijaObrisiActionPerformed
        odabrano = (Osoba) lista.getSelectedValue();
        if(odabrano==null){
            return;
        }
        if(JOptionPane.showConfirmDialog(getParent(), "Jeste li sigurni da želite obrisati " + odabrano.getImePrezime() + "?","Brisanje administratora",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE)==JOptionPane.NO_OPTION){
           return;
       }
       
       if(!obradaAdministratora.obrisi(odabrano)){
           return;
       }
       
       DefaultListModel<Osoba> m = (DefaultListModel<Osoba>) lista.getModel();
       m.removeElement(odabrano);
       lista.repaint();
       cistiPolja();
    }//GEN-LAST:event_akcijaObrisiActionPerformed

    private void akcijaPromijeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_akcijaPromijeniActionPerformed
        if(odabrano==null){
    return;
}       
        reset();
//kontorle
        if(!odabrano.getKorisnicko_ime().equals(korisnicko_ime.getText().trim())){
            if(!obradaAdministratora.provjeraKorisnika(korisnicko_ime.getText().trim())){
                 greska(korisnicko_ime, "Korisničko ime postoji, odaberite drugo korisničko ime");
                return;
            }
            
        }
        if(new String(lozinka.getPassword()).trim().isEmpty() ){
             greska(lozinka, "Lozinka obavezno");
     
        }
        
          if(ime.getText().trim().isEmpty()){
           greska(ime, "Obavezno ime");
           return;
       }
        if(prezime.getText().trim().isEmpty()){
           greska(prezime, "Obavezno prezime");
           return;
       }
        if(uvjet.getText().trim().isEmpty()){
            greska(uvjet, "Obavezno navedite ulogu");
            return;
        }
          if(datumrodenja.getText().trim().length()>0){
        try {
            odabrano.setDatumrodenja(df.parse(datumrodenja.getText().trim().replace(" ","")));
        } catch (Exception e) {
            greska(datumrodenja, "datum nije dobrog formata");
            return;
        }
        }else{
            odabrano.setDatumrodenja(null);
        }
        odabrano=popuni(odabrano);
       odabrano.setKorisnicko_ime(korisnicko_ime.getText().trim());
        odabrano.setLozinka(new String(lozinka.getPassword()).trim());
        odabrano.setIme(ime.getText().trim());
        odabrano.setPrezime(prezime.getText().trim());
        odabrano.setUloga(uvjet.getText().trim());
        
        //promjena u bazi
        if(!obradaAdministratora.promijeni(odabrano)){
            return;
        }
        
        //dodavanje/postavljanje
        //nekada ne radi dobro
        lista.repaint();
         cistiPolja();
         reset();
    }//GEN-LAST:event_akcijaPromijeniActionPerformed

