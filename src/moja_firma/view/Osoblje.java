    private void listaValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listaValueChanged
          reset();
        odabrano = (Osoba) lista.getSelectedValue();
        if(odabrano==null){
            return;
        }
        korisnicko_ime.setText(odabrano.getKorisnicko_ime());
        lozinka.setText(odabrano.getLozinka());
        ime.setText(odabrano.getIme());
        prezime.setText(odabrano.getPrezime());
        naziv.setText(odabrano.getNaziv());
        oib.setText(odabrano.getOib());
        telefon.setText(odabrano.getTelefon());
        mobitel.setText(odabrano.getMob());
        email.setText(odabrano.getEmail());
        adresa.setText(odabrano.getAdresa());
        iban.setText(odabrano.getIban());
        aktivan.setSelected(odabrano.isAktivan());
        primjedba.setText(odabrano.getPrimjedba());
        
        
        SimpleDateFormat df = new SimpleDateFormat(Pomocno.FORMAT_DATUM_UNOS);   
        try {
            datumrodenja.setText(df.format(odabrano.getDatumrodenja()));
        } catch (Exception e) {
            datumrodenja.setText("");
        }
    }//GEN-LAST:event_listaValueChanged

    private void akcijaDodajNoviActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_akcijaDodajNoviActionPerformed
              reset();
        if(korisnicko_ime.getText().trim().isEmpty()){
           greska(korisnicko_ime, "Obavezno korisničko ime");
           return;
       }
        
        if(!obradaAdministratora.provjeraKorisnika(korisnicko_ime.getText().trim())){
             greska(korisnicko_ime, "Korisnik postoji u bazi, odabarite drugi");
           return;
        }
        
        
        if((new String(lozinka.getPassword())).trim().isEmpty()){
           greska(lozinka, "Obavezno lozinka");
           return;
       }
     
       if(ime.getText().trim().isEmpty()){
           greska(ime, "Obavezno ime");
           return;
       }
        if(prezime.getText().trim().isEmpty()){
           greska(prezime, "Obavezno prezime");
           return;
       }
        if(uvjet.getText().trim().isEmpty()){
            greska(uvjet,"Upišite ulogu");
            return;
        }
        
       
        Osoba o = new Osoba();
         o=popuni(o);
        
        o=obradaAdministratora.dodaj(o);
        
        
         
         if(datumrodenja.getText().trim().length()>0){
        try {
            o.setDatumrodenja(df.parse(datumrodenja.getText().trim().replace(" ","")));
        } catch (Exception e) {
            greska(datumrodenja, "datum nije dobrog formata");
            return;
        }
        }else{
            o.setDatumrodenja(null);
        }
        
      
        
                  DefaultListModel<Osoba> m =(DefaultListModel<Osoba>) lista.getModel();

            m.addElement(o);
         lista.repaint();
       cistiPolja();
       reset();
        
    }//GEN-LAST:event_akcijaDodajNoviActionPerformed
   private Osoba popuni(Osoba o){
        o.setIme(ime.getText().trim());
        o.setPrezime(prezime.getText().trim());
        o.setUloga(uvjet.getText().trim());
        o.setKorisnicko_ime(korisnicko_ime.getText().trim());
        o.setLozinka(new String(lozinka.getPassword()));
        o.setNaziv(naziv.getText().trim());
        o.setOib(oib.getText());
        o.setTelefon(telefon.getText().trim());
        o.setMob(mobitel.getText().trim());
        o.setEmail(email.getText().trim());
        o.setAdresa(adresa.getText().trim());
        o.setIban(iban.getText().trim());
        o.setPrimjedba(primjedba.getText().trim());
        o.setAktivan(aktivan.isSelected());
        
        if(datumrodenja.getText().trim().length()>0){
        try {
            o.setDatumrodenja(df.parse(datumrodenja.getText().trim().replace(" ","")));
        } catch (Exception e) {           
        }
        }else{
            o.setDatumrodenja(null);
        } 
        return o;
    }
    
