    private void akcijaPromijeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_akcijaPromijeniActionPerformed
          if(odabrano==null){
            return;
        }
        reset();
        //kontorle
        if(ime.getText().trim().isEmpty()){
            greska(ime, "Obavezno ime");
            return;
        }
       
        if(prezime.getText().trim().isEmpty()){
            greska(prezime, "Obavezno prezime");
            return;
        }
        
        if(uloga.getText().trim().isEmpty()){
            greska(uloga, "Obavezno uloga");
            return;
        }
        
        if(nazivrada.getText().trim().isEmpty()){
            greska(nazivrada, "Obavezno nazivrada");
            return;
        }
         if(korisnicko_ime.getText().trim().isEmpty()){
            greska(korisnicko_ime, "Obavezno korianičko ime");
            return;
        }
         
        
         odabrano=popuni(odabrano);
        
       

        //promjena u bazi
        if(!obrada.promjeni(odabrano)){
            return;
        }
           //promjena u bazi
        if(!obradaAdministratora.promijeni(odabrano)){
            return;
        }

        lista.repaint();
        cistiPolja();
         reset();
    }//GEN-LAST:event_akcijaPromijeniActionPerformed
        
        private Rad popuni(Rad o){
        o.setIme(ime.getText());
        o.setPrezime(prezime.getText());
        o.setNazivrada(nazivrada.getText());
        o.setCijenarada(new BigDecimal(cijenarada.getText().trim()));
        o.setKolicina(new BigDecimal(kolicina.getText().trim()));
        o.setUloga(uloga.getText());
        o.setKorisnicko_ime(korisnicko_ime.getText().trim());
        
        return o;
    }
    private void akcijaDodajNoviActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_akcijaDodajNoviActionPerformed
         reset();
        if(ime.getText().trim().isEmpty()){
            greska(ime, "Obavezno ime");
            return;
        }
       
        if(prezime.getText().trim().isEmpty()){
            greska(prezime, "Obavezno prezime");
            return;
        }
         if(korisnicko_ime.getText().trim().isEmpty()){
            greska(korisnicko_ime, "Obavezno korisničko ime");
            return;
        }                
        if(!obradaAdministratora.provjeraKorisnika(korisnicko_ime.getText().trim())){
             greska(korisnicko_ime, "Korisnik postoji u bazi, odabarite drugi");
           return;
        }

        Rad o = new Rad();
        o=popuni(o);
       
        
        o=obrada.dodaj(o);
        if(o==null){
            //obavijest
            return;
        }
        DefaultListModel<Rad> m =(DefaultListModel<Rad>) lista.getModel();
        m.addElement(o);

    }//GEN-LAST:event_akcijaDodajNoviActionPerformed

    private void akcijaobrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_akcijaobrisiActionPerformed
              odabrano = (Rad) lista.getSelectedValue();
        if(odabrano==null){
            return;
        }
        if(JOptionPane.showConfirmDialog(getParent(), "Jeste li sigurni da želite obrisati " + odabrano.getNaziv() + "?","Brisanje rada",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE)==JOptionPane.NO_OPTION){
           return;
       }
       
       if(!obrada.obrisi(odabrano)){
           return;
       }
       
       DefaultListModel<Rad> m = (DefaultListModel<Rad>) lista.getModel();
       m.removeElement(odabrano);
        lista.repaint();
       cistiPolja();
             
    }//GEN-LAST:event_akcijaobrisiActionPerformed

