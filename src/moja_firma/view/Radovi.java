    private void listaValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listaValueChanged
          reset();
        odabrano = (Rad) lista.getSelectedValue();
        if(odabrano==null){
            return;
        }
        
        ime.setText(odabrano.getIme());
        prezime.setText(odabrano.getPrezime());
        nazivrada.setText(odabrano.getNazivrada());
        uloga.setText(odabrano.getUloga());
        cijenarada.setText(String.valueOf(odabrano.getCijenarada()));
        kolicina.setText(String.valueOf(odabrano.getKolicina()));
        korisnicko_ime.setText(odabrano.getKorisnicko_ime());
        
    }//GEN-LAST:event_listaValueChanged

